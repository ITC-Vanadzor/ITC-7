package com.entities;

public class Rectangle {
    private int sideA;
    private int sideB;
    
    public void setSideA(int a) {
        sideA = a;
    }

    public void setSideB(int b) {
        sideB = b;
    }
    
    public int getSideA() {
        return sideA;
    }

    public int getSideB() {
        return sideB;
    }
}
